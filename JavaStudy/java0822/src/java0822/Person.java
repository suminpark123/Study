package java0822;

public class Person {
	
	
	String name= "학생이름";
	int age = 20;
	
	
	public void Person() {
		
	}
	
	public void Person(String name, int age) {
		this.name= name;
		this.age= age;
	}
	
	public String getName() {
		return name;
	}
	
	
	
	public void setName(String name) {
		this.name = name;
	}
	
	
	public int getAge() {
		return age;
	}
	
	
	
	public void setAge(int age) {
		this.age = age;
	}

	
	
	
	
	
	

}
